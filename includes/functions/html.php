/*----------------------------------------------------------------------------
 Outputs text suitable for displaying on a web page
----------------------------------------------------------------------------*/
function h($text = '')
{
  if(is_array($text)) $text = implode("\n", $text);

  return strtr(htmlspecialchars($text), array("\n" => '<br />', "\r" => ''));
}
